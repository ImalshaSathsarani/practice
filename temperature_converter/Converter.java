    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jRadioButton1.isSelected()){
            jTextField2.setText("C");
            double cel=Double.parseDouble(jTextField1.getText());
            double far=((double)9/5*cel)+32;
            jTextField3.setText(Double.toString(far));
            jTextField4.setText("F");
       }
        if(jRadioButton2.isSelected()){
            jTextField2.setText("F");
            double far=Double.parseDouble(jTextField1.getText());
            double cel=(double)5/9*(far-32);
            jTextField3.setText(Double.toString(cel));
            jTextField4.setText("C");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

